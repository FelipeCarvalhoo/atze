function maiorDeIdade(idade) {  //criamos uma function
    if(idade >= 18){ //se idade for maior ou igual a 18
        return true;//retorna true
    }else{//caso contrario retorna false
        return false;
    }
}

let idade = prompt('Digite sua idade');  //criamos a variavel idade
let verificacao = maiorDeIdade(idade);// criamos  a variavel verificação para arquivar a function maiorDeIdade
if(verificacao) {  // aqui usamos o if para verificar a function, vamos relembrar? se maior de idade for maior que ou igual a 18 retorna true ou false
    console.log('Maior de idade pois você tem: '+idade);
}else{
    console.log('Menor de idade porque '+idade+' '+'e menor que 18')
}








































